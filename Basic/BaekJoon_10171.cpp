// 백준 10171 - 고양이 (특수문자 출력하기)
/*
// 아래와 같이 출력하기 //

\    /\
 )  ( ')
(  /  )
 \(__)|

*/
#include <iostream>
using namespace std;
int main()
{
    cout << "\\    /\\" << "\n";	// 특수문자 '\'
    cout << " )  ( \') " << "\n";	// 특수문자 ''', '\'
    cout << "(  /  )" << "\n";
    cout << " \\(__)|";				// 특수문자 '\'
    return 0;
}
