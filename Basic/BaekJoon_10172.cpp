// 백준 10172 - 개 (특수문자 출력하기)
/*
// 아래와 같이 출력하기 //

|\_/|
|q p|   /}
( 0 )"""\
|"^"`    |
||_/=\\__|

*/
#include <iostream>
using namespace std;
int main()
{
    cout << "|\\_/|" << "\n";			// 특수문자 '\'
    cout << "|q p|   /}" << "\n";
    cout << "( 0 )\"\"\"\\" << "\n";	// 특수문자 '\', '"(큰따옴표)'
    cout << "|\"^\"`    |" << "\n";		// 특수문자 '"'
    cout << "||_/=\\\\__|";				// 특수문자 '\'
    return 0;
}
